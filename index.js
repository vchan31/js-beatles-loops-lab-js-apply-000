function theBeatlesPlay(musicians,instruments){
  var whatTheyPlay = [];
  for (let i = 0; i < musicians.length; i++){
    for (let j = 0; i < instruments.length; j++) {
      whatTheyPlay.push(musicians[i] + " plays " + instruments[j] ) 
        return whatTheyPlay
    } 
  }
}



